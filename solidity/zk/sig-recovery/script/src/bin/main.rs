//! Host program. In `--execute` mode it builds a tiny test batch (1 action
//! signed by a known dev key), runs the circuit inside SP1's zkVM emulator,
//! and asserts the committed public values match what the host computes
//! independently. This is the smoke test for the circuit's digest math —
//! the more thorough byte-equivalence-vs-Solidity fuzz test lives in
//! `bin/digest-equivalence.rs` (next task).
//!
//! In `--prove` mode it generates a real (Groth16-wrapped) proof.
//!
//!   RUST_LOG=info cargo run --release --bin sig-recovery -- --execute
//!   RUST_LOG=info cargo run --release --bin sig-recovery -- --prove

use alloy_sol_types::SolType;
use clap::Parser;
use k256::ecdsa::{signature::hazmat::PrehashSigner, RecoveryId, Signature, SigningKey};
use sig_recovery_lib::{
    action_data_struct_hash, eip712_digest, CircuitInputs, PublicValuesStruct, TinyKeccak,
    UnpackedAction,
};
use sp1_sdk::{
    blocking::{ProveRequest, Prover, ProverClient},
    include_elf, Elf, ProvingKey, SP1Stdin,
};
use tiny_keccak::{Hasher, Keccak};

const SIG_RECOVERY_ELF: Elf = include_elf!("sig-recovery-program");

#[derive(Parser, Debug)]
#[command(author, version, about, long_about = None)]
struct Args {
    #[arg(long)]
    execute: bool,
    #[arg(long)]
    prove: bool,
}

fn keccak256(data: &[u8]) -> [u8; 32] {
    let mut h = Keccak::v256();
    h.update(data);
    let mut out = [0u8; 32];
    h.finalize(&mut out);
    out
}

/// Pack one action with rc=0, ac=0, no text. Same byte layout as
/// packActions.ts / CawActions.sol.
fn pack_action_minimal(
    action_type: u8,
    sender_id: u32,
    receiver_id: u32,
    receiver_cawonce: u32,
    client_id: u32,
    cawonce: u32,
) -> Vec<u8> {
    let mut buf = Vec::with_capacity(25);
    buf.push(action_type);
    buf.extend_from_slice(&sender_id.to_be_bytes());
    buf.extend_from_slice(&receiver_id.to_be_bytes());
    buf.extend_from_slice(&receiver_cawonce.to_be_bytes());
    buf.extend_from_slice(&client_id.to_be_bytes());
    buf.extend_from_slice(&cawonce.to_be_bytes());
    buf.push(0); // rc
    buf.push(0); // ac
    buf.extend_from_slice(&[0, 0]); // textLen = 0
    buf
}

fn pack_actions(slices: &[Vec<u8>]) -> Vec<u8> {
    let mut out = Vec::new();
    out.extend_from_slice(&(slices.len() as u16).to_be_bytes());
    for s in slices {
        out.extend_from_slice(s);
    }
    out
}

fn pack_sig_group(group_size: u16, v: u8, r: &[u8; 32], s: &[u8; 32]) -> Vec<u8> {
    let mut buf = Vec::with_capacity(67);
    buf.extend_from_slice(&group_size.to_be_bytes());
    buf.push(v);
    buf.extend_from_slice(r);
    buf.extend_from_slice(s);
    buf
}

fn pack_sigs(groups: &[Vec<u8>]) -> Vec<u8> {
    let mut out = Vec::new();
    out.extend_from_slice(&(groups.len() as u16).to_be_bytes());
    for g in groups {
        out.extend_from_slice(g);
    }
    out
}

fn signer_address(sk: &SigningKey) -> [u8; 20] {
    let vk = sk.verifying_key();
    let pubkey = vk.to_encoded_point(false);
    let h = keccak256(&pubkey.as_bytes()[1..]);
    let mut addr = [0u8; 20];
    addr.copy_from_slice(&h[12..]);
    addr
}

fn main() {
    sp1_sdk::utils::setup_logger();
    dotenv::dotenv().ok();

    let args = Args::parse();
    if args.execute == args.prove {
        eprintln!("Error: specify exactly one of --execute or --prove");
        std::process::exit(1);
    }

    // Build a minimal test batch:
    //   1 action, no recipients, no amounts, no text.
    //   Signed by a deterministic dev key.
    let action_bytes = pack_action_minimal(
        0,           // actionType = caw
        42,          // senderId
        0, 0,        // receiverId, receiverCawonce
        1,           // clientId
        7,           // cawonce
    );
    let packed_actions = pack_actions(&[action_bytes.clone()]);

    // Domain separator. Real on-chain value is generated by
    // CawActions.generateDomainHash() using the contract address + chain id.
    // For this smoke test we just pick an arbitrary 32-byte value.
    let domain_separator: [u8; 32] = [0xAB; 32];

    // Compute the EIP-712 digest off-chain (same path the circuit will take).
    let action = UnpackedAction {
        action_type: 0,
        sender_id: 42,
        receiver_id: 0,
        receiver_cawonce: 0,
        client_id: 1,
        cawonce: 7,
        recipients: vec![],
        amounts: vec![],
        text: &[],
        slice_start: 2,                       // header is 2 bytes
        slice_end: 2 + action_bytes.len(),
    };
    let struct_hash = action_data_struct_hash(&TinyKeccak, &action);
    let digest = eip712_digest(&TinyKeccak, &domain_separator, &struct_hash);

    // Sign with a deterministic test key (NOT a real key, just for the smoke test).
    let sk_bytes: [u8; 32] = [
        0xac, 0x09, 0x74, 0xbe, 0xc3, 0x9a, 0x17, 0xe3, 0x6b, 0xa4, 0xa6, 0xb4, 0xd2, 0x38,
        0xff, 0x94, 0x4b, 0xac, 0xb4, 0x78, 0xcb, 0xed, 0x5e, 0xfc, 0xae, 0x78, 0x4d, 0x7b,
        0xf4, 0xf2, 0xff, 0x80,
    ];
    let sk = SigningKey::from_slice(&sk_bytes).unwrap();
    let signer_addr = signer_address(&sk);
    println!("dev signer address: 0x{}", hex::encode(signer_addr));

    let (sig, recovery_id): (Signature, RecoveryId) =
        sk.sign_prehash(&digest).expect("sign failed");
    let r: [u8; 32] = sig.r().to_bytes().into();
    let s: [u8; 32] = sig.s().to_bytes().into();
    let v: u8 = recovery_id.to_byte() + 27;

    let group = pack_sig_group(1, v, &r, &s);
    let packed_sigs = pack_sigs(&[group]);

    // Compute expected public values off-chain.
    let expected_packed_actions_hash = keccak256(&packed_actions);
    let expected_packed_sigs_hash = keccak256(&packed_sigs);
    let expected_signers_hash = keccak256(&signer_addr); // 1 signer, 20 bytes

    let inputs = CircuitInputs {
        packed_actions: packed_actions.clone(),
        packed_sigs: packed_sigs.clone(),
        domain_separator,
    };

    let client = ProverClient::from_env();
    let mut stdin = SP1Stdin::new();
    stdin.write(&inputs);

    if args.execute {
        let (output, report) = client.execute(SIG_RECOVERY_ELF, stdin).run().unwrap();
        println!("Program executed. Cycles: {}", report.total_instruction_count());

        let pv = PublicValuesStruct::abi_decode(output.as_slice()).unwrap();
        let pa: [u8; 32] = pv.packedActionsHash.0;
        let ps: [u8; 32] = pv.packedSigsHash.0;
        let sg: [u8; 32] = pv.signersHash.0;
        let dom: [u8; 32] = pv.domainSeparator.0;

        assert_eq!(pa, expected_packed_actions_hash, "packedActionsHash mismatch");
        assert_eq!(ps, expected_packed_sigs_hash, "packedSigsHash mismatch");
        assert_eq!(sg, expected_signers_hash, "signersHash mismatch");
        assert_eq!(dom, domain_separator, "domainSeparator mismatch");
        println!("✅ All four public-input commitments match host-computed values.");
    } else {
        let pk = client.setup(SIG_RECOVERY_ELF).expect("setup failed");
        let proof = client.prove(&pk, stdin).run().expect("prove failed");
        client
            .verify(&proof, pk.verifying_key(), None)
            .expect("verify failed");
        println!("✅ Proof generated and verified.");
    }
}
